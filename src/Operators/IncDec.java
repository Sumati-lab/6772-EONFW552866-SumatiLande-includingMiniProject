package Operators;

public class IncDec 
{

	public static void main(String[] args)
	{
		
	int a= 50;
	int b=100;
	
	System.out.println("Print value of a is:  "  +a);
	System.out.println("Print value of b is:  "  +b);
	
	
	System.out.println("--a is:  "  +(--a));
	System.out.println(" a-- is:  "  +(a--));
	System.out.println("++b is:  "  +(++b));
	System.out.println("b-- is:  "  +(b--));
	
	
}
}
