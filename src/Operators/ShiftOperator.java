package Operators;

public class ShiftOperator {

	public static void main(String[] args) {
		
int a=50;

System.out.println("SIGNED VALUE OF A RIGHT SHIFT IS "  +(a>>5));
System.out.println("SIGNED VALUE OF A LEFT SHIFT IS "  +(a<<5));
System.out.println("UNSIGNED VALUE OF A RIGHT SHIFT IS "  +(a>>>5));

	}

}
