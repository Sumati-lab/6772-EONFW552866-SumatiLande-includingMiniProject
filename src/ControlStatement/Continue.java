package ControlStatement;

public class Continue {

	public static void main(String[] args) {
		// continue statement is  used to only jumps from the current iteration.
	
		
		for(int i= 0; i<=13; i++)
		
	{
		if(i==10)
			
			continue;
		
		System.out.println(+i);
	
	}
	
	
	
	
	}

}
